Du kannst jetzt mit cat DATEINAME Dateien lesen.

Bei kurzen DATEINAMEN ist es kein Problem das zu
tippen. Vielleicht hast du schon gemerkt, dass
viele Befehle schön kurz sind? Das liegt daran,
dass wir Computermenschen oft faul sind. ;)

